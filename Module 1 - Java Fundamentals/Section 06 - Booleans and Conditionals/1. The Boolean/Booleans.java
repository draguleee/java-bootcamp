/**
 * Booleans: used to store true or false (1 bit of memory)
 * 
 * Remember:
 * - Storing booleans is pretty useless!
 * - Booleans are useful when expressed in form of comparisons
 */

public class Booleans {
    public static void main(String[] args) {
        
        // Variable bool1 and bool2 of type boolean
        boolean bool1 = true;
        boolean bool2 = false;

        // Print the variables
        System.out.println(bool1);
        System.out.println(bool2);

    }
    
}