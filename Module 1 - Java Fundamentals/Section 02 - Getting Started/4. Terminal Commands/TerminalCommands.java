/*
 * Buttons:
 * 1. "Run" button: runs the program.
 * 2. "Debug" button: debugs the program from a breakpoint.
 * 
 * Commands:
 * 1. javac (Java Compiler) - compiles Java code into Byte code
 * 2. java - executes the compiled code
 */


public class TerminalCommands {
    public static void main(String[] args) {
        System.out.println("Terminal commands: ");
        System.out.println("\t - javac: compiles the Java code into Byte code");   
        System.out.println("\t - java: executes the compiled code");   

    }
}