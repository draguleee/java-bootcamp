// Intro to variables

public class Variables {
    public static void main(String[] args) {
        
        // Variables
        int people = 20;                // Lowercase - integer
        int peopleOnBus = 10;           // Lower Camel Case - integer
        int busTickets = peopleOnBus;   // Lower Camel Case - integer

        // Print the value of the variable
        System.out.println("Number of people: " + people);
        System.out.println("Number of passengers: " + peopleOnBus);
        System.out.println("Number of tickets: " + busTickets);

    }
}